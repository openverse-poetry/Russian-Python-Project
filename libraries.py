def создать_словарь(**kwargs) -> Dict:
    """Создает словарь из именованных аргументов."""
    return kwargs

def получить_ключ(словарь: Dict, ключ: Any, по_умолчанию: Any = None) -> Any:
    """Получает значение по ключу или значение по умолчанию."""
    return словарь.get(ключ, по_умолчанию)

def ключи(словарь: Dict) -> List:
    """Возвращает список ключей словаря."""
    return list(словарь.keys())

def значения(словарь: Dict) -> List:
    """Возвращает список значений словаря."""
    return list(словарь.values())

def пары(словарь: Dict) -> List[кортеж]:
    """Возвращает список пар (ключ, значение)."""
    return list(словарь.items())

