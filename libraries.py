def выход(код: int = 0) -> None:
    """Завершает выполнение программы."""
    sys.exit(код)

def время_сек() -> float:
    """Возвращает текущее время в секундах (timestamp)."""
    return time.time()

def пауза(секунды: float) -> None:
    """Приостанавливает выполнение на указанное количество секунд."""
    time.sleep(секунды)

def система(команда: str) -> int:
    """Выполняет системную команду. Возвращает код возврата."""
    return os.system(команда)

def текущая_папка() -> str:
    """Возвращает путь к текущей рабочей директории."""
    return os.getcwd()

def сменить_папку(путь: str) -> None:
    """Меняет текущую рабочую директорию."""
    try:
        os.chdir(путь)
    except FileNotFoundError:
        raise FileNotFoundError(f"Папка не найдена: {путь}")

def существует_файл(путь: str) -> bool:
    """Проверяет наличие файла или папки."""
    return os.path.exists(путь)

