def печать(*args: Any, sep: str = ' ', end: str = '\n') -> None:
    """
    Выводит аргументы в стандартный поток вывода.
    Аналог print().
    """
    print(*args, sep=sep, end=end)

def ввод(приглашение: str = "") -> str:
    """
    Считывает строку из стандартного потока ввода.
    Аналог input().
    """
    try:
        return input(приглашение)
    except EOFError:
        return ""

def чтение_файла(путь: str, кодировка: str = 'utf-8') -> str:
    """
    Читает содержимое файла целиком.
    """
    try:
        with open(путь, 'r', encoding=кодировка) as f:
            return f.read()
    except FileNotFoundError:
        raise FileNotFoundError(f"Файл не найден: {путь}")
    except Exception as e:
        raise IOError(f"Ошибка при чтении файла: {e}")

def запись_файла(путь: str, содержимое: str, кодировка: str = 'utf-8') -> None:
    """
    Записывает строку в файл (перезаписывая его).
    """
    try:
        with open(путь, 'w', encoding=кодировка) as f:
            f.write(содержимое)
    except Exception as e:
        raise IOError(f"Ошибка записи в файл: {e}")

def добавить_в_файл(путь: str, содержимое: str, кодировка: str = 'utf-8') -> None:
    """
    Добавляет строку в конец файла.
    """
    try:
        with open(путь, 'a', encoding=кодировка) as f:
            f.write(содержимое)
    except Exception as e:
        raise IOError(f"Ошибка при добавлении в файл: {e}")

# ===================================================================
# 2. Преобразование типов
# ===================================================================

def целое(значение: Any) -> int:
    """Преобразует значение в целое число."""
    try:
        return int(значение)
    except (ValueError, TypeError):
        raise ValueError(f"Невозможно преобразовать '{значение}' в целое число")

def дробное(значение: Any) -> float:
    """Преобразует значение в число с плавающей точкой."""
    try:
        return float(значение)
    except (ValueError, TypeError):
        raise ValueError(f"Невозможно преобразовать '{значение}' в дробное число")

def строка(значение: Any) -> str:
    """Преобразует значение в строку."""
    return str(значение)

def логическое(значение: Any) -> bool:
    """Преобразует значение в логический тип."""
    return bool(значение)

def список(значение: Any = None) -> List:
    """Создает список или преобразует итерируемый объект в список."""
    if значение is None:
        return []
    try:
        return list(значение)
    except TypeError:
        raise TypeError(f"Объект '{значение}' не является итерируемым")

# ===================================================================
# 3. МАТЕМАТИКА
# ===================================================================

def модуль(число: float) -> float:
    """Возвращает модуль числа."""
    return abs(число)

def округлить(число: float, знаков: int = 0) -> float:
    """Округляет число до указанного количества знаков."""
    return round(число, знаков)

def минимум(*аргументы: float) -> float:
    """Возвращает минимальное значение из аргументов."""
    if not аргументы:
        raise ValueError("Требуется хотя бы один аргумент")
    return min(аргументы)

def максимум(*аргументы: float) -> float:
    """Возвращает максимальное значение из аргументов."""
    if not аргументы:
        raise ValueError("Требуется хотя бы один аргумент")
    return max(аргументы)

def сумма(список_чисел: List[float]) -> float:
    """Возвращает сумму элементов списка."""
    return sum(список_чисел)

def степень(основание: float, показатель: float) -> float:
    """Возводит число в степень."""
    return math.pow(основание, показатель)

def корень(число: float) -> float:
    """Возвращает квадратный корень."""
    if число < 0:
        raise ValueError("Корень из отрицательного числа не определен в вещественных числах")
    return math.sqrt(число)

def синус(угол: float) -> float:
    """Синус угла (в радианах)."""
    return math.sin(угол)

def косинус(угол: float) -> float:
    """Косинус угла (в радианах)."""
    return math.cos(угол)

def тангенс(угол: float) -> float:
    """Тангенс угла (в радианах)."""
    return math.tan(угол)

def пи() -> float:
    """Возвращает число Пи."""
    return math.pi

def случайное(мин: float = 0.0, макс: float = 1.0) -> float:
    """Возвращает случайное число в диапазоне [мин, макс]."""
    return random.uniform(мин, макс)

def случайное_целое(мин: int, макс: int) -> int:
    """Возвращает случайное целое число в диапазоне [мин, макс]."""
    return random.randint(мин, макс)

# ===================================================================
# 4. СТРОКИ
# ===================================================================

def длина(объект: Union[str, List, Dict]) -> int:
    """Возвращает длину строки, списка или словаря."""
    return len(объект)

def верхний_регистр(строка: str) -> str:
    """Преобразует строку в верхний регистр."""
    return строка.upper()

def нижний_регистр(строка: str) -> str:
    """Преобразует строку в нижний регистр."""
    return строка.lower()

def обрезать(строка: str) -> str:
    """Удаляет пробелы в начале и в конце строки."""
    return строка.strip()

def заменить(строка: str, старое: str, новое: str) -> str:
    """Заменяет все вхождения подстроки."""
    return строка.replace(старое, новое)

def разделить(строка: str, разделитель: str = " ") -> List[str]:
    """Разбивает строку на список подстрок."""
    return строка.split(разделитель)

def соединить(список: List[str], разделитель: str = "") -> str:
    """Объединяет список строк в одну строку."""
    return разделитель.join(список)

def содержит(строка: str, подстрока: str) -> bool:
    """Проверяет, содержится ли подстрока в строке."""
    return подстрока in строка

def начинается_с(строка: str, префикс: str) -> bool:
    """Проверяет, начинается ли строка с префикса."""
    return строка.startswith(префикс)

def заканчивается_на(строка: str, суффикс: str) -> bool:
    """Проверяет, заканчивается ли строка суффиксом."""
    return строка.endswith(суффикс)

def найти(строка: str, подстрока: str) -> int:
    """Возвращает индекс первого вхождения подстроки или -1."""
    return строка.find(подстрока)

# ===================================================================
# 5. СПИСКИ И КОЛЛЕКЦИИ
# ===================================================================

def добавить(список: List, элемент: Any) -> None:
    """Добавляет элемент в конец списка."""
    список.append(элемент)

def вставить(список: List, индекс: int, элемент: Any) -> None:
    """Вставляет элемент по указанному индексу."""
    список.insert(индекс, элемент)

def удалить(список: List, элемент: Any) -> None:
    """Удаляет первое вхождение элемента из списка."""
    try:
        список.remove(элемент)
    except ValueError:
        raise ValueError(f"Элемент '{элемент}' не найден в списке")

def получить_по_индексу(список: List, индекс: int) -> Any:
    """Получает элемент по индексу."""
    try:
        return список[индекс]
    except IndexError:
        raise IndexError(f"Индекс {индекс} вне диапазона списка")

def отсортировать(список: List, в_обратном_порядке: bool = False) -> List:
    """Возвращает отсортированный список (не изменяет исходный)."""
    return sorted(список, reverse=в_обратном_порядке)

def развернуть(список: List) -> List:
    """Возвращает список в обратном порядке."""
    return список[::-1]

def диапазон(стоп: int) -> List[int]:
    """Генерирует список чисел от 0 до стоп-1."""
    return list(range(стоп))

def диапазон_от(начало: int, конец: int, шаг: int = 1) -> List[int]:
    """Генерирует список чисел от start до stop с шагом."""
    return list(range(начало, конец, шаг))

